########Laterals

#######Integer
print(2)
print("2")
#Expected output = 2

######Octal and Hexadecimal Numbers
print(0x123) #0x - Hexadecimal Number
print(0o342) #0o - Octal Number

######Float
print(0.4) #Floating-Number
#Expected output = 0.4
print("4.4") #Floating-Number
#Expected output = 4.4
print("44") #Integer-Number
#Expected output = 44

#######Exponent
print(3e8) #e is exponent
#Expected output = 300000000.0
print(4E5) #E is exponent
#Expected output = 400000.0
print(0.0000000000000000000001)
#Expected output = 1e-22
print(0.00005)
#Expected output = 5e-05

#####Strings
print("This is \"Abhishek\"") #Backslash acts as escaper
#Expected output = This is "Abhishek"
print("This is 'Abhishek'")
#Expected output = This is 'Abhishek'
print("I\'m Abhi")
#Expected output = I'm Abhi

###Boolean Values
